public class ComparisonOperators {
  public static void main(String[] args) {
      int x = 1;
      int y = 1;
      int z = 3;

      System.out.println("x == y: " + (x == y));
      System.out.println("x != y: " + (x != y)); 
      System.out.println("x > y: " + (x > y));
      System.out.println("x < z: " + (x < z));
      System.out.println("x >= y: " + (x >= y));
      System.out.println("x <= z: " + (x <= z));
  }
}
